# isalpha() ->It returns true if all the characters are alphabets and there is at least one character, otherwise False.
print("It returns true if all the characters are alphabets and there is at least one character, otherwise False.")
basic=name.isalpha()
print(basic)
print(type(basic))

# islower() -> It returns true if the characters of a string are in lower case, otherwise false.
print("It returns true if the characters of a string are in lower case, otherwise false.")
basic=name.islower()
print(basic)
print(type(basic))

# isnumeric() -> It returns true if the string contains only numeric characters.
print("It returns true if the string contains only numeric characters.")
basic=name.isnumeric()
print(basic)
print(type(basic))

# isprintable() -> It returns true if all the characters of s are printable or s is empty, false otherwise.
print("It returns true if all the characters of s are printable or s is empty, false otherwise.")
basic=name.isprintable()
print(type(basic))
print(basic)

# isupper() -> It returns false if characters of a string are in Upper case, otherwise False.
print("It returns false if characters of a string are in Upper case, otherwise False.")
basic=name.isupper()
print(basic)
print(type(basic))





